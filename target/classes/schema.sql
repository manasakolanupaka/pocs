DROP TABLE IF EXISTS TBL_EMPLOYEES;
DROP TABLE IF EXISTS TBL_DEPARTMENT;

DROP TABLE IF EXISTS TBL_DEPT_EMPLOYEE;
  
CREATE TABLE TBL_EMPLOYEES (
    id INT AUTO_INCREMENT  PRIMARY KEY,
    first_name VARCHAR(250) NOT NULL,
    last_name VARCHAR(250) NOT NULL,
    email VARCHAR(250) DEFAULT NULL
);

CREATE TABLE TBL_DEPARTMENT
(
  id INT AUTO_INCREMENT PRIMARY KEY,
  department_name VARCHAR(250) NOT NULL
);

create table TBL_DEPT_EMPLOYEE 
( employee_id  INT NOT NULL, department_id INT NOT NULL,
PRIMARY KEY (employee_id, department_id) 
);

CREATE TABLE TBL_ROLE
(
id INT AUTO_INCREMENT PRIMARY KEY,
  role VARCHAR(100) NOT NULL
);

CREATE TABLE TBL_ROLE_EMPLOYEE
( employee_id  INT NOT NULL, role_id VARCHAR(100) NOT NULL,
PRIMARY KEY (employee_id, role_id) 
);

CREATE TABLE TBL_INSURANCE
(
  id INT AUTO_INCREMENT PRIMARY KEY,employee_id INT NOT NULL,
  insurance_plan VARCHAR(100),insurance_amount INT NOT NULL,
  status VARCHAR(250) NOT NULL
);

CREATE TABLE TBL_AUDIT(
id INT AUTO_INCREMENT,
action VARCHAR(100),
body VARCHAR(100)
);

